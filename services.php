				<p>
				Heat therapy works to increase blood circulation which promotes healing <br> and relaxing of your muscles.  Heat therapy is great for stiff joints and for <br> relaxing muscles prior to sports or exercising.
				However, heat can also <br> increase swelling in an injured area if it is used too soon after the injury <br> so you need to be careful. Your Physio will recommend a suitable time, <br> placement and duration for heat therapy.<br><br>
				Heat Therapy is also used in conjunction with other Physio treatment programmes to relieve pain such as Rheumatic pain, Sciatica, Lumbago, Sprains and Muscle stiffness. Depending on your needs, our Chartered Physiotherapists 
				can determine if and when Heat Therapy is right for you. 
